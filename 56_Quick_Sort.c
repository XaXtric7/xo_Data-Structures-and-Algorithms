// ✅ Advantages of Quick Sort:

// Fast on Large Data Sets – Quick Sort has an average-case time complexity of O(n log n), making it much faster than Bubble Sort, Selection Sort, and Insertion Sort for large inputs.
// Efficient for Randomly Ordered Data – Performs well even when the data is unsorted.
// In-Place Sorting – Requires very little extra memory (O(log n) space in the best case).
// Divide and Conquer Approach – Breaks the problem into smaller subproblems, making it efficient.
// Cache-Friendly – Works well with modern CPU caches due to sequential memory access.
// Widely Used in Real-World Applications – Many programming libraries and frameworks use Quick Sort for its efficiency.

// ❌ Disadvantages of Quick Sort:.

// Worst-Case Time Complexity is O(n²) – If the pivot selection is poor (e.g., always picking the smallest or largest element), performance degrades to O(n²).
// Not a Stable Sort – It does not always maintain the relative order of equal elements, which may be an issue for certain applications.
// Recursive Calls Use Stack Space – Quick Sort is recursive, so deep recursion can lead to stack overflow in some cases.
// Performance Depends on Pivot Choice – A bad pivot selection can lead to imbalanced partitions, making the algorithm inefficient.